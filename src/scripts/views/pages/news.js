const News = {
  render: async () => {
    let newsHTML = '';

    newsData.forEach((berita) => {
      // eslint-disable-next-line object-curly-newline
      const { id, title, image, content } = berita;

      // eslint-disable-next-line prefer-template
      const truncatedContent = content.substring(0, 100) + '...';

      newsHTML += `
        <div class="beritaItem" data-id="${id}">
          <a href="#/detail-news?id=${id}">
            <img src="${image}" alt="Gambar Berita">
            <h5>${title}</h5>
            <p>${truncatedContent}</p>
            <button class="showMoreButton">Tampilkan Lebih Banyak</button>
            <button class="showLessButton" style="display: none;">Tampilkan Lebih Sedikit</button>
          </a>
        </div>
      `;
    });

    return `
      <div class="newsPage">
        <h2>Berita Terbaru Pertanian</h2>
        <div class="beritaContainer">
          ${newsHTML}
        </div>
      </div>
    `;
  },

  afterRender: async () => {
    const showMoreButtons = document.querySelectorAll('.beritaItem .showMoreButton');
    const showLessButtons = document.querySelectorAll('.beritaItem .showLessButton');
    // eslint-disable-next-line no-trailing-spaces
    
    showMoreButtons.forEach((button) => {
      button.addEventListener('click', (event) => {
        event.preventDefault();
        const beritaItem = button.closest('.beritaItem');
        const contentParagraph = beritaItem.querySelector('p');
        const fullContent = newsData.find((berita) => berita.id === beritaItem.dataset.id).content;
        contentParagraph.textContent = fullContent;
        // eslint-disable-next-line no-param-reassign
        button.style.display = 'none';
        beritaItem.querySelector('.showLessButton').style.display = 'inline-block';
      });
    });
    // eslint-disable-next-line no-trailing-spaces
    
    showLessButtons.forEach((button) => {
      button.addEventListener('click', (event) => {
        event.preventDefault();
        const beritaItem = button.closest('.beritaItem');
        const contentParagraph = beritaItem.querySelector('p');
        // eslint-disable-next-line prefer-template
        const truncatedContent = contentParagraph.textContent.substring(0, 100) + '...';
        contentParagraph.textContent = truncatedContent;
        // eslint-disable-next-line no-param-reassign
        button.style.display = 'none';
        beritaItem.querySelector('.showMoreButton').style.display = 'inline-block';
      });
    });
  },
};

// eslint-disable-next-line eol-last
export default News;